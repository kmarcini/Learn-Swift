func onTarget(_ actual: Int) -> String {
    let target = 8
    
    if actual >= (target - 1) && actual <= (target + 1) {
        return "On target!"
    } else if actual < (target - 1) {
        return "Below target!"
    } else {
        return "Above target, don't try too hard!"
    }
}

